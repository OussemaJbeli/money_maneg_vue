import total_cards from '@/Components/cards/total_cards.vue';
import lists from '@/Components/cards/lists.vue';
import target_limit from '@/Components/cards/target_limit.vue';
import { Head } from '@inertiajs/vue3';
import BarChart from '@/Components/sharts/BarChart.vue';
</script>

<script>
export default {
    name: 'App',
    components: {
        Head,
        BarChart,
    },
    props: {
        date: Object,
        today: Object,
        week: Object,
        month_chart: Object,
        month: Object,
        items: Object,
        regions_tab: Object,
        chart: Object,
        runk_Tickets: Object,
        runk_items: Object,
        runk_region: Object,
        runk_states: Object,
        runk_currency: Object,
        Target_limit: Object,
    },

    data() {
        return {
            chartData_horizontal: [
                { label: 'Item 1', value: 30 },
                { label: 'Item 2', value: 50 },
                { label: 'Item 3', value: 70 },
                { label: 'Item 4', value: 40 },
                { label: 'Item 4', value: 40 },
            ],
            infoBoxContent: '',
            infoBoxVisible: false,
            chartData: {
                labels: this.chart.thisMonth_select.number_days,
                datasets: [{
                    label: this.$page.props.auth.user.main_currency+' in one day',
                    backgroundColor: '#0363e0',
                    data: this.shart_data_fun1(),
                }]
            },
            chartData1: {
                labels: this.chart.lastMonth_select.number_days,
                datasets: [{
                    label: this.$page.props.auth.user.main_currency+' in one day',
                    backgroundColor: '#0363e0',
                    data: this.shart_data_fun2(),
                }]
            },
            chartOptions: {
                responsive: true
            },
            form: this.$inertia.form({
                selectedOption: 'this Month',
                selectedTop10: 'by price',
            }),
            id_limit: 0,
            show_select_chart: 'this Month',
            openLimit_add_frame: false,
            limit_form_add: this.$inertia.form({
                selectedOption_add: "monthly",
                date_to_add: null,
                date_from_add: null,
                value_add: null,
                currency_add: null,
            }),
        }
    },
    mounted() {
        // Attach event listeners
        document.querySelectorAll('path, circle').forEach(element => {
        element.addEventListener('mouseenter', this.showInfoBox);
        element.addEventListener('mouseleave', this.hideInfoBox);
        });

        document.addEventListener('mousemove', this.updateInfoBoxPosition);
    },
    methods: {
        shart_data_fun1(){
            switch (this.$page.props.auth.user.main_currency) {
                case 'TND':
                    return this.chart.thisMonth_select.TND
                case 'USD':
                    return this.chart.thisMonth_select.USD
                case 'EUR':
                    return this.chart.thisMonth_select.EUR
                default:
                    break;
            }
        },
        shart_data_fun2(){
            switch (this.$page.props.auth.user.main_currency) {
                case 'TND':
                    return this.chart.lastMonth_select.TND
                case 'USD':
                    return this.chart.lastMonth_select.USD
                case 'EUR':
                    return this.chart.lastMonth_select.EUR
                default:
                    break;
            }
        },
        openLimit_add() {
            if(this.openLimit_add_frame)
                this.openLimit_add_frame=false;
            else{
                this.openLimit_add_frame=true;
                if(this.Target_limit.target_data){
                    this.id_limit = this.Target_limit.target_data.id;
                }
            }
        },
        add_plane() {
            this.limit_form_add.get(`/Limit/${this.id_limit}/create_dachboard`, {
                preserveScroll: true,
                onSuccess: () => {
                this.openLimit_add_frame = false;
                this.limit_form_add.reset();
                },
            });
        },
        showInfoBox(event) {
            this.infoBoxContent = event.target.dataset.info;
            this.infoBoxVisible = true;
        },
        hideInfoBox() {
            this.infoBoxVisible = false;
        },
        updateInfoBoxPosition(event) {
            if (this.infoBoxVisible) {
                const infoBox = document.getElementById('info-box');
                infoBox.style.top = event.pageY - infoBox.offsetHeight - 120 + 'px';
                infoBox.style.left = event.pageX - infoBox.offsetWidth*3 + 'px';
            }
        }
    },
}
</script>